
export function generateRandomHumanName(): string {
    const num_adjectives = adjectives.length
    const num_colors = colors.length
    const adj = adjectives[Math.floor(Math.random() * num_adjectives)];
    const color = colors[Math.floor(Math.random() * num_colors)];

    return `${adj}-${color}-slug`;
}



